class TestRecommender(BaseTopiqTestCase):
    """Recommendation engine tests for query matching and result limits."""

    def setUp(self):
        """Create base fixtures and initialize the recommender."""
        super().setUp()
        self.recommender = ResourceRecommender()

    def test_exact_match(self):
        """Exact topic queries should match the expected topic."""
        result = search_topics("Deadlock")
        self.assertTrue(result["found"])
        self.assertEqual(result["matched_topic_name"], "Deadlock")

    def test_case_insensitive_match(self):
        """Case-insensitive searches should still find the correct topic."""
        result = search_topics("DEADLOCK")
        self.assertTrue(result["found"])

    def test_partial_match(self):
        """Partial phrase matches like 'dead lock' should still resolve."""
        result = search_topics("dead lock")
        self.assertTrue(result["found"])

    def test_synonym_match(self):
        """Keyword-based synonym searches should match binary tree content."""
        result = search_topics("tree data structure")
        self.assertTrue(result["found"])
        self.assertEqual(result["matched_topic_name"], "Binary Tree")

    def test_unknown_topic_returns_not_found(self):
        """Unknown topics should return a safe not-found response."""
        result = search_topics("xyzabcnonexistent123")
        self.assertFalse(result["found"])

    def test_returns_max_3_videos(self):
        """Recommender should never return more than three video results."""
        result = search_topics("Deadlock")
        self.assertLessEqual(len(result["videos"]), 3)

    def test_returns_max_3_readings(self):
        """Recommender should never return more than three reading results."""
        result = search_topics("Deadlock")
        self.assertLessEqual(len(result["readings"]), 3)


class TestManagementCommand(TestCase):
    """Basic smoke tests for the seed_data management command."""

    def test_seed_data_demo_creates_records(self):
        """Demo seed mode should populate a lightweight dataset."""
        call_command("seed_data", "--flush", "--demo")
        self.assertEqual(Semester.objects.count(), 6)
        self.assertEqual(Subject.objects.count(), 8)
        self.assertGreaterEqual(Topic.objects.count(), 6)
        self.assertGreaterEqual(VideoResource.objects.count(), 18)
        self.assertGreaterEqual(ReadingResource.objects.count(), 18)

    def test_seed_data_uses_working_youtube_search_links(self):
        """Seeded demo video records should use safe YouTube search URLs."""
        call_command("seed_data", "--flush", "--demo")
        video = VideoResource.objects.first()
        self.assertIsNotNone(video)
        self.assertIn("youtube.com/results?search_query=", video.youtube_url)
        self.assertIsNone(video.youtube_video_id)
